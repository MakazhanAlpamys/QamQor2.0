import { Outlet, NavLink, Link } from 'react-router-dom';
import { useState, useEffect } from 'react';
import { FaEdit, FaComments, FaUser, FaArrowLeft } from 'react-icons/fa';
import '../../styles/Layout.css';

const AdminLayout = () => {
  const [activeTab, setActiveTab] = useState('/admin');
  
  useEffect(() => {
    // Get current path
    const path = window.location.pathname;
    setActiveTab(path);
  }, []);
  
  const handleNavClick = (path) => {
    setActiveTab(path);
  };
  
  return (
    <div className="layout-container">
      <div className="admin-header">
        <div className="admin-header-content">
          <Link to="/" className="back-to-user">
            <FaArrowLeft />
            <span>Пайдаланушы режиміне</span>
          </Link>
          <h1>QamQor Админ</h1>
        </div>
      </div>
      
      <div className="content-container">
        <Outlet />
      </div>
      
      <nav className="bottom-nav">
        <NavLink
          to="/admin"
          className={`nav-item ${activeTab === '/admin' ? 'active' : ''}`}
          onClick={() => handleNavClick('/admin')}
          end
        >
          <FaEdit className="nav-icon" />
          <span>Контент</span>
        </NavLink>
        
        <NavLink
          to="/admin/feedback"
          className={`nav-item ${activeTab === '/admin/feedback' ? 'active' : ''}`}
          onClick={() => handleNavClick('/admin/feedback')}
        >
          <FaComments className="nav-icon" />
          <span>Кері байланыс</span>
        </NavLink>
        
        <NavLink
          to="/admin/profile"
          className={`nav-item ${activeTab === '/admin/profile' ? 'active' : ''}`}
          onClick={() => handleNavClick('/admin/profile')}
        >
          <FaUser className="nav-icon" />
          <span>Профиль</span>
        </NavLink>
      </nav>
    </div>
  );
};

export default AdminLayout; 